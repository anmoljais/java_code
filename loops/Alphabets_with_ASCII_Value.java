package loops;

import java.util.Scanner;

public class Alphabets_with_ASCII_Value {
    public static void main(String[] args) {

        for(int i=65;i<=90;i++){
            System.out.println(i +" " +(char)i);
        }
        System.out.println();
        for(int j=97;j<=112;j++){
            System.out.println(j+" "+(char)j);
        }
    }
}
